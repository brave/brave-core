"""Read and update the version numbers in `//chrome/VERSION`.

Brave overloads chrome/VERSION: MAJOR keeps the upstream Chromium milestone,
while MINOR/BUILD/PATCH hold the Brave version (major/minor/build). The `gen`
subcommand generates `//brave/build/version.gni` for gn, while the `update`
subcommand writes the Brave version into MINOR/BUILD/PATCH, and maintains a
`.chromium` sidecar with the original content.
"""

from __future__ import annotations

import argparse
import datetime
import re
import subprocess
import sys
from pathlib import Path

# Matches a full chrome/VERSION file, capturing each component. Any unexpected
# content (e.g. a newly added key) is rejected so format changes are caught.
_VERSION_FILE_RE = re.compile(r'MAJOR=(?P<MAJOR>\d+)\n'
                              r'MINOR=(?P<MINOR>\d+)\n'
                              r'BUILD=(?P<BUILD>\d+)\n'
                              r'PATCH=(?P<PATCH>\d+)\n')

# Matches a 'major.minor.build' Brave version, capturing the three parts.
_BRAVE_VERSION_RE = re.compile(r'(\d+)\.(\d+)\.(\d+)')

# Template for a chrome/VERSION file, rendered with a MAJOR/MINOR/BUILD/PATCH
# dict.
_VERSION_FILE_TEMPLATE = """\
MAJOR=%(MAJOR)s
MINOR=%(MINOR)s
BUILD=%(BUILD)s
PATCH=%(PATCH)s
"""

# Path of the generated gn file, i.e. //brave/build/version.gni.
_VERSION_GNI = Path(__file__).resolve().parents[1] / 'version.gni'

# Template for the generated //brave/build/version.gni.
_VERSION_GNI_TEMPLATE = """\
# Copyright (c) %(year)s The Brave Authors. All rights reserved.
# This Source Code Form is subject to the terms of the Mozilla Public
# License, v. 2.0. If a copy of the MPL was not distributed with this file,
# You can obtain one at https://mozilla.org/MPL/2.0/.

# Generated by //brave/build/util/version.py. Do not edit.

brave_version_major = "%(brave_version_major)s"
brave_version_minor = "%(brave_version_minor)s"
brave_version_build = "%(brave_version_build)s"

# The full Brave version.
brave_version = "%(brave_version)s"

# The Chromium milestone (the MAJOR component of the upstream version).
chromium_version_major = "%(chromium_version_major)s"

# The full Chrome version.
chrome_version_string = "%(chrome_version_string)s"
"""


def _parse_version(contents: str) -> dict[str, str]:
    """Parses and validates the components of a chrome/VERSION file.

    Args:
        contents: The text of a chrome/VERSION file.

    Returns:
        A dict mapping MAJOR, MINOR, BUILD and PATCH to their string values.

    Raises:
        ValueError: If `contents` is not a well-formed chrome/VERSION file.
    """
    match = _VERSION_FILE_RE.fullmatch(contents)
    if not match:
        raise ValueError(f'Unexpected chrome/VERSION format:\n{contents}')
    return match.groupdict()


def _read_version_from_file(path: Path) -> dict[str, str]:
    """Reads and validates the components of a chrome/VERSION file.

    Args:
        path: The path of the chrome/VERSION file to read.

    Returns:
        A dict mapping MAJOR, MINOR, BUILD and PATCH to their string values.
    """
    return _parse_version(path.read_text(encoding='utf-8'))


def _write_if_changed(path: Path, contents: str) -> None:
    """Writes `contents` to `path`, but only if that would change the file.

    Args:
        path: The path of the file to write.
        contents: The desired contents of the file.
    """
    if not path.exists() or path.read_text(encoding='utf-8') != contents:
        path.write_text(contents, encoding='utf-8', newline='')


class Versioner:
    """A Brave-patched chrome/VERSION file and its upstream sidecar.

    This class is used to manage the version file in Chromium. It's main role is
    to update the version file with the Brave version, and to keep a sidecar
    `.chromium` version file with the original Chromium version.

    This class also provides support code so `gn` can retrieve the version
    values from the codebase for both the Brave version (from the patched file)
    and the Chromium version (from the .chromium sidecar).
    """

    def __init__(self, version_file: str | Path) -> None:
        """Initializes the versioner.

        Args:
            version_file: The path of the chrome/VERSION file to manage.
        """
        self._version_file = Path(version_file)

    @property
    def _sidecar(self) -> Path:
        """Path to the unpatched upstream chrome/VERSION.chromium sidecar."""
        return self._version_file.with_name(f'{self._version_file.name}'
                                            '.chromium')

    def _git_head_contents(self) -> str:
        """Returns the version file's contents as committed at git HEAD.

        Returns:
            The unpatched upstream contents of the version file, i.e. the true
            Chromium version Brave is built on top of.
        """
        version_file = self._version_file.resolve()

        # The repo-relative pathspec git expects (always forward slashes).
        toplevel = subprocess.check_output([
            'git', '-C',
            str(version_file.parent), 'rev-parse', '--show-toplevel'
        ],
                                           encoding='utf-8').strip()
        pathspec = version_file.relative_to(toplevel).as_posix()

        return subprocess.check_output(
            ['git', '-C', toplevel, 'show', f'HEAD:{pathspec}'],
            encoding='utf-8')

    def gen(self) -> None:
        """Generates //brave/build/version.gni from the version files.

        Reads the Brave version from the patched chrome/VERSION and the Chromium
        version from the .chromium sidecar, then writes the matching gn
        variables. The file is written only when its contents change.
        """
        patched = _read_version_from_file(self._version_file)
        upstream = _read_version_from_file(self._sidecar)
        values = {
            'year': datetime.date.today().year,

            # Brave version lives in the patched MINOR/BUILD/PATCH on disk.
            'brave_version_major': patched['MINOR'],
            'brave_version_minor': patched['BUILD'],
            'brave_version_build': patched['PATCH'],
            'brave_version': (f"{patched['MINOR']}.{patched['BUILD']}."
                              f"{patched['PATCH']}"),

            # The upstream Chromium milestone and full version come from the
            # .chromium sidecar.
            'chromium_version_major': upstream['MAJOR'],
            'chrome_version_string': (
                f"{upstream['MAJOR']}.{upstream['MINOR']}."
                f"{upstream['BUILD']}.{upstream['PATCH']}"),
        }
        _write_if_changed(_VERSION_GNI, _VERSION_GNI_TEMPLATE % values)

    def update(self, brave_version: str) -> None:
        """Writes the Brave version into chrome/VERSION and the sidecar.

        chrome/VERSION keeps MAJOR (the Chromium milestone, read from git HEAD)
        and receives the Brave version in MINOR/BUILD/PATCH. The .chromium
        sidecar receives the unpatched upstream version from git HEAD. Each file
        is written only when its contents change.

        Args:
            brave_version: The Brave version to write, as 'major.minor.build'.

        Raises:
            ValueError: If `brave_version` is not a 'major.minor.build' string.
        """
        match = _BRAVE_VERSION_RE.fullmatch(brave_version)
        if not match:
            raise ValueError(
                "expected a 'major.minor.build' Brave version, got "
                f'{brave_version!r}')

        # MAJOR (the Chromium milestone) comes from the unpatched git HEAD copy;
        # MINOR/BUILD/PATCH hold the Brave major/minor/build.
        upstream = self._git_head_contents()
        current = _parse_version(upstream)
        values = {
            'MAJOR': current['MAJOR'],
            'MINOR': match.group(1),
            'BUILD': match.group(2),
            'PATCH': match.group(3),
        }
        _write_if_changed(self._version_file, _VERSION_FILE_TEMPLATE % values)
        _write_if_changed(self._sidecar, upstream)

    @staticmethod
    def gen_command(options: argparse.Namespace) -> None:
        """Runs the `gen` subcommand.

        Args:
            options: The parsed command-line arguments.
        """
        Versioner(options.version_file).gen()

    @staticmethod
    def update_command(options: argparse.Namespace) -> None:
        """Runs the `update` subcommand.

        Args:
            options: The parsed command-line arguments.
        """
        Versioner(options.version_file).update(options.brave_version)


def main() -> int:
    """Parses command-line arguments and runs the selected subcommand.

    Returns:
        The process exit code.
    """
    parser = argparse.ArgumentParser(description=__doc__)
    subparsers = parser.add_subparsers(dest='command', required=True)

    # Shared version-file argument for all subcommands.
    version_file_parser = argparse.ArgumentParser(add_help=False)
    version_file_parser.add_argument('version_file',
                                     type=Path,
                                     help='Path to the chrome/VERSION file.')

    gen_parser = subparsers.add_parser(
        'gen',
        parents=[version_file_parser],
        help='Generate //brave/build/version.gni from chrome/VERSION.')
    gen_parser.set_defaults(func=Versioner.gen_command)

    update_parser = subparsers.add_parser(
        'update',
        parents=[version_file_parser],
        help="Write the Brave version into chrome/VERSION's MINOR/BUILD/PATCH."
    )
    update_parser.add_argument(
        '--brave-version',
        required=True,
        help="The Brave version to write, as 'major.minor.build'.")
    update_parser.set_defaults(func=Versioner.update_command)

    options = parser.parse_args()
    options.func(options)


if __name__ == '__main__':
    sys.exit(main())
