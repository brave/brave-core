// This is a CommonJS module so we can use __dirname to resolve the current
// directory. The Storybook version we use doesn't handle import.meta.dirname.

const fs = require('node:fs')

let dirName = __dirname
// Use fs.realpathSync to normalize the path(__dirname could be c:\.. or C:\..).
if (process.platform === 'win32') {
  dirName = fs.realpathSync.native(dirName)
}

module.exports = dirName
