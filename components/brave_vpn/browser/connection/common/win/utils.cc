#include "brave/components/brave_vpn/browser/connection/common/win/utils.h"

namespace brave_vpn {

HRESULT HRESULTFromLastError() {
  const auto error_code = ::GetLastError();
  return (error_code != NO_ERROR) ? HRESULT_FROM_WIN32(error_code) : E_FAIL;
}

}  // namespace brave_vpn
